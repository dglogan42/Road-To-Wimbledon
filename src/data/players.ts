export function getPlayersByTour(tour: 'atp' | 'wta' | 'all'): Player[] {
  if (tour === 'atp') return atpPlayers;
  if (tour === 'wta') return wtaPlayers;
  return [...atpPlayers, ...wtaPlayers];
}

export function countGrandSlamWins(player: Player): number {
  const slams = ['australian-open', 'roland-garros', 'wimbledon', 'us-open'];
  return player.results.filter(
    (r) => slams.includes(r.tournamentId) && r.outcome === 'champion',
  ).length;
}

export function getPlayerRoadResult(player: Player, tournamentId: string): string | null {
  const result = player.results.find((r) => r.tournamentId === tournamentId);
  if (!result) return null;
  const labels: Record<string, string> = {
    champion: 'W',
    finalist: 'F',
    semifinal: 'SF',
    quarterfinal: 'QF',
    'round-of-16': 'R16',
    lost: '—',
  };
  return labels[result.outcome] ?? result.round;
}